function isIntersect(point, elem)
{
	if (point.x > elem.offsetLeft && point.x < elem.offsetLeft + elem.width && point.y > elem.offsetTop && point.y < elem.offsetTop + elem.height) {
		
		return true;
	}
	else
		return false;
}

